alias h="history"
alias c="clear"
alias x="exit"

alias ..="cd .."
alias ...="cd ../.."
alias ....="cd ../../.."

alias reload="source ~/.bashrc"

alias ls="ls --color=auto"
alias grep="grep --color=auto"
alias fgrep="fgrep --color=auto"
alias egrep="egrep --color=auto"

alias l="ls -CF"
alias la="ls -A"
alias ll="ls -alF"
